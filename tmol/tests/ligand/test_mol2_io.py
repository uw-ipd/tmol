def test_atom_array_from_mol2_coords_match_mol2():
    import numpy as np

    arr = atom_array_from_mol2(REF1_MOL2, res_name="LG1")
    mol2_xyzs = np.array(
        [row["xyz"] for row in _parse_mol2_atom_block(REF1_MOL2)], dtype=np.float32
    )
    assert np.allclose(arr.coord, mol2_xyzs, atol=1e-5)


def test_atom_array_from_mol2_annotations():
    arr = atom_array_from_mol2(REF1_MOL2, res_name="LG1", chain_id="L", res_id=999)
    assert set(arr.res_name) == {"LG1"}
    assert set(arr.chain_id) == {"L"}
    assert set(arr.res_id.tolist()) == {999}
    assert all(arr.hetero)


def test_atom_array_from_mol2_has_bonds():
    arr = atom_array_from_mol2(REF1_MOL2, res_name="LG1")
    assert arr.bonds is not None
    assert arr.bonds.get_bond_count() == 55  # ref1.mol2 declares 55 bonds


def test_atom_array_from_mol2_rename_atoms_aligns_with_restype():
    """With rename_atoms=True on both sides, names still align."""
    arr = atom_array_from_mol2(REF1_MOL2, res_name="LG1", rename_atoms=True)
    restype, _ = prepare_ligand_from_mol2(
        REF1_MOL2, res_name="LG1", rename_atoms=True
    )
    assert list(arr.atom_name) == [a.name for a in restype.atoms]


def test_write_params_from_mol2_round_trip(tmp_path):
    """write_params_from_mol2 -> read_params_file preserves atoms and bonds."""
    out = tmp_path / "ref1_out.params"
    write_params_from_mol2(REF1_MOL2, out, res_name="LG1")
    restype = read_params_file(out)

    expected_atoms = [(n, t) for n, t, _ in _params_atom_lines(ROSETTA_REF1_PARAMS)]
    actual_atoms = [(a.name, a.atom_type) for a in restype.atoms]
    assert actual_atoms == expected_atoms

    expected_bonds = _params_bond_set(ROSETTA_REF1_PARAMS)
    actual_bonds = {frozenset((a, b)) for a, b, _ in restype.bonds}
    assert actual_bonds == expected_bonds
