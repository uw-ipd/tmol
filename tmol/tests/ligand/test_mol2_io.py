def test_atom_names_preserved_by_default():
    """Default rename_atoms=False: all heavy + H names match the mol2 column."""
    restype, _ = prepare_ligand_from_mol2(REF1_MOL2, res_name="LG1")
    mol2_names = [row["name"] for row in _parse_mol2_atom_block(REF1_MOL2)]
    assert [a.name for a in restype.atoms] == mol2_names


def test_atom_names_regenerated_with_flag():
    """rename_atoms=True: H names follow H<bonded_element><count> convention."""
    restype, _ = prepare_ligand_from_mol2(
        REF1_MOL2, res_name="LG1", rename_atoms=True
    )
    names = [a.name for a in restype.atoms]
    # Heavy atoms in ref1.mol2 already use <elem><n> convention, so they
    # are unchanged; only H names should differ (H1->HC1/HO1/HN1/HR1).
    h_names = [n for n in names if n.startswith("H")]
    assert h_names, "expected hydrogens in the residue"
    for n in h_names:
        assert len(n) >= 3 and n[1].isalpha(), (
            f"H name {n!r} should follow H<elem><n> convention "
            "under rename_atoms=True"
        )
    mol2_h_names = {
        row["name"]
        for row in _parse_mol2_atom_block(REF1_MOL2)
        if row["name"].startswith("H")
    }
    assert set(h_names) != mol2_h_names, "H names should differ from the mol2 names"


def test_atom_count_matches_mol2():
    restype, _ = prepare_ligand_from_mol2(REF1_MOL2, res_name="LG1")
    assert len(restype.atoms) == 51


