@pytest.mark.parametrize(
    "mol2_path, rosetta_params_path, res_name",
    [
        (REF1_MOL2, ROSETTA_REF1_PARAMS, "LG1"),
        (REF2_MOL2, ROSETTA_REF2_PARAMS, "LG1"),
    ],
    ids=["ref1", "ref2"],
)
def test_atom_names_match_rosetta(mol2_path, rosetta_params_path, res_name):
    """Atom names emitted by the tmol mol2 path equal Rosetta's atom names."""
    restype, _ = prepare_ligand_from_mol2(mol2_path, res_name=res_name)
    actual = [a.name for a in restype.atoms]
    expected = [n for n, _, _ in _params_atom_lines(rosetta_params_path)]
    assert actual == expected


@pytest.mark.parametrize(
    "mol2_path, rosetta_params_path, res_name",
    [
        (REF1_MOL2, ROSETTA_REF1_PARAMS, "LG1"),
        (REF2_MOL2, ROSETTA_REF2_PARAMS, "LG1"),
    ],
    ids=["ref1", "ref2"],
)
def test_atom_types_match_rosetta(mol2_path, rosetta_params_path, res_name):
    """tmol's atom-type classifier output matches Rosetta's per-atom types."""
    restype, _ = prepare_ligand_from_mol2(mol2_path, res_name=res_name)
    actual = [(a.name, a.atom_type) for a in restype.atoms]
    expected = [(n, t) for n, t, _ in _params_atom_lines(rosetta_params_path)]
    assert actual == expected


@pytest.mark.parametrize(
    "mol2_path, rosetta_params_path, res_name",
    [
        (REF1_MOL2, ROSETTA_REF1_PARAMS, "LG1"),
        (REF2_MOL2, ROSETTA_REF2_PARAMS, "LG1"),
    ],
    ids=["ref1", "ref2"],
)
def test_charges_match_rosetta(mol2_path, rosetta_params_path, res_name):
    """Per-atom charges match Rosetta's params (which preserves mol2 charges)."""
    _, charges = prepare_ligand_from_mol2(mol2_path, res_name=res_name)
    expected = {n: q for n, _, q in _params_atom_lines(rosetta_params_path)}
    for name, q in expected.items():
        assert name in charges, f"missing charge for {name}"
        assert abs(charges[name] - q) < 1e-3, (
            f"charge for {name}: got {charges[name]}, expected {q}"
        )


@pytest.mark.parametrize(
    "mol2_path, rosetta_params_path, res_name",
    [
        (REF1_MOL2, ROSETTA_REF1_PARAMS, "LG1"),
        (REF2_MOL2, ROSETTA_REF2_PARAMS, "LG1"),
    ],
    ids=["ref1", "ref2"],
)
def test_bond_set_matches_rosetta(mol2_path, rosetta_params_path, res_name):
    """Bond connectivity (undirected pairs) matches Rosetta's params."""
    restype, _ = prepare_ligand_from_mol2(mol2_path, res_name=res_name)
    actual = {frozenset((a, b)) for a, b, _ in restype.bonds}
    expected = _params_bond_set(rosetta_params_path)
    assert actual == expected


