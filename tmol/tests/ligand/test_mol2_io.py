def test_no_dimorphite_invoked(monkeypatch):
    """The mol2 path must not call dimorphite_dl protonation."""
    import tmol.ligand.dimorphite_dl as dd

    def _boom(*args, **kwargs):
        raise AssertionError("dimorphite_dl must not be called on the mol2 path")

    monkeypatch.setattr(dd, "protonate_mol_variants", _boom)
    restype, _ = prepare_ligand_from_mol2(REF1_MOL2, res_name="LG1")
    assert len(restype.atoms) == 51


def test_no_mmff94_charge_recompute(monkeypatch):
    """The mol2 path must not invoke OBChargeModel to recompute charges."""
    from openbabel import openbabel

    original_find_type = openbabel.OBChargeModel.FindType

    def _boom(name):
        raise AssertionError(
            "OBChargeModel.FindType must not be called on the mol2 path "
            f"(tried to load {name!r})"
        )

    monkeypatch.setattr(openbabel.OBChargeModel, "FindType", staticmethod(_boom))
    try:
        _, charges = prepare_ligand_from_mol2(REF1_MOL2, res_name="LG1")
    finally:
        monkeypatch.setattr(
            openbabel.OBChargeModel, "FindType", staticmethod(original_find_type)
        )
    mol2_rows = _parse_mol2_atom_block(REF1_MOL2)
    for row in mol2_rows:
        assert abs(charges[row["name"]] - row["charge"]) < 1e-4


