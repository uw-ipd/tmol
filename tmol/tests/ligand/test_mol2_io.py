def test_charges_preserved_from_mol2_default_names():
    """Returned charges equal the mol2 column-9 values."""
    _, charges = prepare_ligand_from_mol2(REF1_MOL2, res_name="LG1")
    mol2_rows = _parse_mol2_atom_block(REF1_MOL2)
    for row in mol2_rows:
        assert row["name"] in charges, f"missing charge for {row['name']}"
        assert abs(charges[row["name"]] - row["charge"]) < 1e-4, (
            f"charge for {row['name']}: got {charges[row['name']]}, "
            f"expected {row['charge']}"
        )


def test_coordinates_preserved_in_read():
    """OBMol coords from read_mol2 equal mol2 column 3-5 verbatim."""
    mol = read_mol2(REF1_MOL2)
    mol2_xyzs = [row["xyz"] for row in _parse_mol2_atom_block(REF1_MOL2)]
    from openbabel import openbabel

    for i, obatom in enumerate(openbabel.OBMolAtomIter(mol.OBMol)):
        x, y, z = obatom.GetX(), obatom.GetY(), obatom.GetZ()
        ex, ey, ez = mol2_xyzs[i]
        assert abs(x - ex) < 1e-6 and abs(y - ey) < 1e-6 and abs(z - ez) < 1e-6, (
            f"coord mismatch at atom {i}: got ({x}, {y}, {z}), "
            f"expected ({ex}, {ey}, {ez})"
        )


