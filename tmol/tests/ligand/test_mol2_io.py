"""Tests for the non-destructive mol2 -> tmol params entry point.

Validates that ``prepare_ligand_from_mol2`` reads a mol2 verbatim:
atom names, 3D coordinates, partial charges, and bond orders all flow
through without regeneration. Also covers the ``rename_atoms`` flag and
end-to-end comparison against Rosetta's ``mol2genparams.py`` output.

The Rosetta reference files (``rosetta_ref1.params``, ``rosetta_ref2.params``)
were generated by running
``mol2genparams.py -s ref{1,2}.mol2`` on the matching ``.mol2`` test data,
so they reflect Rosetta's default behavior (preserve mol2 atom names).
"""

from pathlib import Path

import pytest

from tmol.ligand.mol2_io import (
    atom_array_from_mol2,
    parse_mol2_atom_names,
    prepare_ligand_from_mol2,
    read_mol2,
    write_params_from_mol2,
)
from tmol.ligand.params_io import read_params_file


GROUND_TRUTH_DIR = Path(__file__).parent.parent / "data" / "ligand_ground_truth"
REF1_MOL2 = GROUND_TRUTH_DIR / "ref1.mol2"
REF2_MOL2 = GROUND_TRUTH_DIR / "ref2.mol2"
ROSETTA_REF1_PARAMS = GROUND_TRUTH_DIR / "rosetta_ref1.params"
ROSETTA_REF2_PARAMS = GROUND_TRUTH_DIR / "rosetta_ref2.params"


def _parse_mol2_atom_block(path: Path) -> list[dict]:
    """Return one dict per TRIPOS ATOM row with name, xyz, charge."""
    rows: list[dict] = []
    in_atom = False
    with open(path) as fh:
        for line in fh:
            stripped = line.strip()
            if stripped.startswith("@<TRIPOS>"):
                in_atom = stripped == "@<TRIPOS>ATOM"
                continue
            if not in_atom or not stripped:
                continue
            cols = stripped.split()
            if len(cols) < 9:
                continue
            rows.append(
                {
                    "name": cols[1],
                    "xyz": (float(cols[2]), float(cols[3]), float(cols[4])),
                    "sybyl": cols[5],
                    "charge": float(cols[8]),
                }
            )
    return rows


def _params_atom_lines(path: Path) -> list[tuple[str, str, float]]:
    """Return [(name, atom_type, charge), ...] from ATOM lines in a params file."""
    out: list[tuple[str, str, float]] = []
    with open(path) as fh:
        for line in fh:
            cols = line.split()
            if cols and cols[0] == "ATOM" and len(cols) >= 5:
                out.append((cols[1], cols[2], float(cols[4])))
    return out


def _params_bond_set(path: Path) -> set[frozenset]:
    """Return the set of {a, b} undirected pairs from BOND/BOND_TYPE lines."""
    out: set[frozenset] = set()
    with open(path) as fh:
        for line in fh:
            cols = line.split()
            if cols and cols[0] in ("BOND", "BOND_TYPE") and len(cols) >= 3:
                out.add(frozenset((cols[1], cols[2])))
    return out


