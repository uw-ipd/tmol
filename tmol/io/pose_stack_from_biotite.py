@validate_args
def pose_stack_from_biotite(
    biotite_structure,
    torch_device,
    **kwargs,
) -> PoseStack:
    """Build a PoseStack from the output generated by Biotite"""
    from tmol.io.pose_stack_construction import pose_stack_from_canonical_form

    cf, resnames = canonical_form_from_biotite(biotite_structure, torch_device)

    co = canonical_ordering_for_biotite(resnames)
    pbt = packed_block_types_for_biotite(resnames, cf.coords.device)

    return pose_stack_from_canonical_form(co, pbt, *cf, **kwargs)


