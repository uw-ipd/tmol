def canonical_form_from_biotite(biotite_structure, torch_device) -> CanonicalForm:
    """The canonical form is intended to represent a stable, serializable intermediate format
    for a structure so that it can be created today and then be read in years from now
    and be used to construct a PoseStack in tmol. As residue types are integers,
    this means that we must guarantee stability of these integer representations, but it also
    means that you the user must build a PoseStack using the carefully constructed objects
    returned by the canonical_ordering_for_rosettafold2 and packed_block_types_for_rosettafold2
    functions.
    """

    device = torch_device
    n_poses = 1
    if biotite_structure is biotite.structure.AtomArrayStack:
        n_poses = biotite_structure.coord.shape[0]

    biotite_residue_starts = biotite.structure.get_residue_starts(biotite_structure)
    biotite_chain_id_for_res = biotite.structure.chains.get_all_chain_positions(
        biotite_structure
    )[biotite_residue_starts]
    biotite_res_ind_for_atom = biotite.structure.get_all_residue_positions(
        biotite_structure
    )  # res_id
    biotite_name_for_atom = biotite_structure.atom_name
    biotite_res_name_for_atom = biotite_structure.res_name

    # get the tmol residue indices
    biotite_residues = biotite.structure.get_residues(biotite_structure)[1]
    biotite_residue_names = numpy.unique(biotite_residues)

    co = canonical_ordering_for_biotite(biotite_residue_names)

    tmol_restypes = [
        co.restype_io_equiv_classes.index(i_3lc) for i_3lc in biotite_residues
    ]
    atom_mapping = [
        co.restypes_atom_index_mapping[resname][atname]
        for resname, atname in zip(biotite_res_name_for_atom, biotite_name_for_atom)
    ]

    tmol_coords = torch.full(
        (n_poses, len(biotite_residues), co.max_n_canonical_atoms, 3),
        numpy.nan,
        dtype=torch.float32,
        device=device,
    )
    pose_inds = torch.zeros(
        (len(biotite_structure.res_id),), dtype=torch.int32, device=torch_device
    )
    res_inds = torch.tensor(biotite_res_ind_for_atom, device=torch_device)
    atom_inds = torch.tensor(atom_mapping, device=torch_device)
    biotite_coords = torch.tensor(biotite_structure.coord, device=torch_device)

    print(pose_inds.shape)
