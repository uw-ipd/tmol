@validate_args
def pose_stack_from_biotite(
    biotite_structure,
    torch_device,
    **kwargs,
) -> PoseStack:
    """Build a PoseStack from the output generated by Biotite"""
    from tmol.io.pose_stack_construction import pose_stack_from_canonical_form
    from tmol.pack.build_missing_sidechains import (
        build_missing_sidechains_with_missing_atoms,
        create_dunbrack_sampler_from_database,
    )

    cf = canonical_form_from_biotite(biotite_structure, torch_device)

    co = canonical_ordering_for_biotite()
    pbt = packed_block_types_for_biotite(cf.coords.device)

    # Call pose_stack_from_canonical_form with return_block_has_missing_atoms=True
    # to get the missing atoms tensor
    result = pose_stack_from_canonical_form(
        co, pbt, *cf, return_block_has_missing_atoms=True, **kwargs
    )

    # Extract pose stack and missing atoms tensor from result
    if isinstance(result, tuple):
        pose_stack = result[0]
        block_has_missing_atoms = result[
            -1
        ]  # block_has_missing_atoms is the last element
    else:
        pose_stack = result
        block_has_missing_atoms = None

    # If there are missing atoms, build the missing sidechains
    if block_has_missing_atoms is not None and torch.any(block_has_missing_atoms):
        db = _paramdb_for_biotite()

        # Create beta2016 score function
        sfxn = beta2016_score_function(torch_device)

        # Create DunbrackChiSampler from default database
        dunbrack_sampler = create_dunbrack_sampler_from_database(db, torch_device)

        # Build missing sidechains
        pose_stack = build_missing_sidechains_with_missing_atoms(
            pose_stack,
            sfxn,
            dunbrack_sampler,
            block_has_missing_atoms,
            _restype_set_for_biotite(),
        )

    return pose_stack


@validate_args
def biotite_from_pose_stack(pose_stack):
    co = canonical_ordering_for_biotite()
    cf = canonical_form_from_pose_stack(co, pose_stack)
    return biotite_from_canonical_form(cf)


